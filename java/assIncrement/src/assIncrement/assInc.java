package assIncrement;

public class assInc {
	
	public static void main(String args[]) {
		
		int tuna = 5;
		int bass = 12;
		
		System.out.print("Tuna before incrementing is: ");
		System.out.println(tuna);
		tuna++;
		System.out.print("Tuna after incrementing is: ");
		System.out.println(tuna);
		
		System.out.print("Bass before incrementing is: ");
		System.out.println(bass);
		bass--;
		System.out.print("Bass after incrementing is: ");
		System.out.println(bass);
	}

}

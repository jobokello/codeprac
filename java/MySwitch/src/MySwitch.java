
public class MySwitch {
	
	public static void main(String args[]) {
		
		int age = 1;
		
		switch(age) {
		
		case 3: 
			System.out.print("this is true");
			break;
		case 1: 
			System.out.print("this number is smaller");
			break;
		default: 
			System.out.print("that does not make sense");
			break;
		}
	}

}
